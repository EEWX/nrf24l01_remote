void LCD19264_Init(void)
{
  GPIO_Init(LCD19264_SCK_GPIO,LCD19264_SCK_PIN,GPIO_Mode_Out_PP_Low_Slow);
  GPIO_Init(LCD19264_SDA_GPIO,LCD19264_SDA_PIN,GPIO_Mode_Out_PP_Low_Slow);
  GPIO_Init(LCD19264_RST_GPIO,LCD19264_RST_PIN,GPIO_Mode_Out_PP_Low_Slow);
  GPIO_Init(LCD19264_CD_GPIO,LCD19264_CD_PIN,GPIO_Mode_Out_PP_Low_Slow);
  GPIO_Init(LCD19264_CS_GPIO,LCD19264_CS_PIN,GPIO_Mode_Out_PP_Low_Slow);
  GPIO_Init(LCD19264_LED_GPIO,LCD19264_LED_PIN,GPIO_Mode_Out_PP_Low_Slow);
  
  //---------TIM2 also used to triger ADC-----------
  //CLK_PeripheralClockConfig(CLK_Peripheral_TIM2,ENABLE);
  //TIM2_TimeBaseInit(TIM2_Prescaler_1,TIM2_CounterMode_Up,16000);//1000Hz
  TIM2_OC1Init(TIM2_OCMode_PWM1,TIM2_OutputState_Enable,8000,TIM2_OCPolarity_High,TIM2_OCIdleState_Reset);
  TIM2_CCxCmd(TIM2_Channel_1,ENABLE);
  TIM2_CtrlPWMOutputs(ENABLE);
  //TIM2_Cmd(ENABLE);
  
  atomTimerDelay(15);
  LCD_RST_RESET;
  atomTimerDelay (10);
  LCD_CS_SET;
  LCD_A0_SET;
  LCD_RST_SET;
  atomTimerDelay (10);
  LCD_Write_REG(0x2f);
  LCD_Write_REG(0xaf);	//display ON
  LCD_Write_REG(0xa0);	//Normal direction mode
  LCD_SetVolumn(36);
  LCD_SetIndicator(0x02);
  LCD_DisplayStartAdr(0);
  LCD_ClearScreen(0x00);
  
}
void LCD_Write_REG(unsigned char data)
{
	char cnt = 0;
	LCD_CS_RESET;
	LCD_A0_RESET;
	for(cnt = 0;cnt<8;cnt++)
	{
		LCD_SCK_RESET;
		//delay(1);
		if(data&0x80)
		{
			LCD_SDI_SET;
		}
		else
		{
			LCD_SDI_RESET;
		}
		data<<=1;
		//delay(1);
		LCD_SCK_SET;
		//delay(1);
	}
	LCD_CS_SET;
}

void LCD_Write_RAM(unsigned char data)
{
	char cnt = 0;
	LCD_CS_RESET;
	LCD_A0_SET;
	for(cnt = 0;cnt<8;cnt++)
	{
		LCD_SCK_RESET;
	//	delay(1);
		if(data&0x80)
		{
			LCD_SDI_SET;
		}
		else
		{
			LCD_SDI_RESET;
		}
		data<<=1;
	//	delay(1);
		LCD_SCK_SET;
	//	delay(1);
	}
	LCD_CS_SET;
}

//----------------0-131-------------------
void LCD_WriteColumnAdr(unsigned char adr)
{
	LCD_Write_REG(0x10| (adr>>4));
	LCD_Write_REG(adr&0x0f);
}

//--------------0-8-------------------
void LCD_WritePageAdr(unsigned char adr)
{
	LCD_Write_REG(0xb0| (adr & 0x0f));
}

void LCD_DisplayStartAdr(unsigned char adr)
{
	LCD_Write_REG(0x40| (adr & 0x3f));
}

//-------------0-63---------------------------
void LCD_SetVolumn(unsigned char vol)
{
	LCD_Write_REG(0x81);
	LCD_Write_REG(vol&0x3f);
}

void LCD_SetIndicator(unsigned char vol)
{
	LCD_Write_REG(0xad);
	LCD_Write_REG(vol&0x03);
}

//-----------------X:0-127, Y:0-7 Dot: 0 off, 1 on---------------------
void LCD_DisplayByte(uint8_t x, uint8_t y, uint8_t data)
{
	LCD_WriteColumnAdr(x);
	LCD_WritePageAdr(y);
	LCD_Write_RAM(data);
}

void LCD_ClearScreen(unsigned char dat)
{
	unsigned char page,column;
	
	for(page = 0;page<9;page++)
	{
		LCD_WritePageAdr(page);
		LCD_WriteColumnAdr(0);
		for(column = 0;column<128;column++)
		{
			LCD_Write_RAM(dat);
		}
	}
	
}

//-------------x: 0-127, Y: 0-7
void LCD_Put6x8Char(uint8_t x, uint8_t y, char asc)
{
	uint8_t cnt = 0;
	for(cnt = 0; cnt<6; cnt++)
	{
	
          LCD_DisplayByte(x+cnt,y,(LCD_6x8ASCTable[asc - 32][cnt]));
			
	}
	
}
//-------------x: 0-127, Y: 0-7
void LCD_Put6x8Str(uint8_t x, uint8_t y, char *ptr)
{
	uint8_t length = strlen(ptr);
	while(length--)
	{
		LCD_Put6x8Char(x,y,*ptr);
		x+=6;
		ptr++;
	}
	
}

void LCD_Put8x16Char(uint8_t x, uint8_t y, char asc)
{
	uint8_t cnt = 0;
	for(cnt = 0; cnt<16; cnt++)
	{
		if(cnt<8)	//------high lines
		{
			
                    LCD_DisplayByte(x+cnt,y+1,(LCD_8x16ASCTable[asc - 32][cnt]));
		
		}
		else
		{
			
			
                    LCD_DisplayByte(x+cnt-8,y,(LCD_8x16ASCTable[asc - 32][cnt]));
			
			
		}
	}
	
}

void LCD_Put8x16Str(uint8_t x, uint8_t y, char *ptr)
{
	uint8_t length = strlen(ptr);
	while(length--)
	{
		LCD_Put8x16Char(x,y,*ptr);
		x+=8;
		ptr++;
	}
	
}

void LCD_Put6x8IntNumber(uint8_t x, uint8_t y, uint16_t num)
{
  char temp[5] = {0};
  char cnt;
  temp[0] = num/10000+48; //convert to ASC
  temp[1] = num%10000/1000+48; //convert to ASC
  temp[2] = num%1000/100+48; //convert to ASC
  temp[3] = num%100/10+48; //convert to ASC
  temp[4] = num%10+48; //convert to ASC
  for(cnt = 0;cnt<5;cnt++)
  {
    LCD_Put6x8Char(x+cnt*6,y,temp[cnt]);
  }
}

