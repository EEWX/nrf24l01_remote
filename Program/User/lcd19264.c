void LCD_Draw8BitBar(uint8_t x1, uint8_t x2, uint8_t y, uint8_t cursor)
{
  uint8_t cnt = 0;
  LCD_DisplayByte(x1,y,0xff);
  LCD_DisplayByte(x2,y,0xff);
  LCD_DisplayByte(x1+cursor,y,0xff);
  for(cnt = x1+1; cnt<x2; cnt++)
  {
    LCD_DisplayByte(cnt,y,0x81);       
  }
  LCD_DisplayByte(x1+cursor,y,0xff);
}

